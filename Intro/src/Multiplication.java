
public class Multiplication { // treinando uso do FOR com tabuada

	public static void main(String[] args) {
		int n = 5;
	    
	    for (int i = 1; i <= 10; i++) {// lembrando que a variável de controle é declarada na inicialização e condição
	      System.out.println(n + " x " + i + " = " + n * i);
	    }

	}

}
